      <h3>Katie</h3>
      <p>Sometimes when people ask how Dan and I met, I say we met in prison. Really, we met at Libertyville High School, which felt kind of like prison in 1999.</p>
      <p>As a sophomore in Señora Rogala's level 2 Spanish class, I had bangs and carried a cross-body canvas purse with a Pink Floyd patch pinned to it. Dan was a junior with spiky hair, baggy pants, and a quiet coolness detectable from day one. I was into it, not that Dan had any idea.</p>
      <p>Outside of Spanish class, Dan was my favorite guitar player in all of Lake County (in all of anywhere). I organized the local ?band jams,? and always made sure to give his bands the longest sets and best placement on the bill.</p>
      <p>I also asked him to give me guitar lessons, securing at least one solid hour of quality time each week. But Dan was a horrible teacher. I learned to play about 15 seconds of two songs.</p>
      <p>Our growing friendship continued through college. Dan went to the Savannah College of Art and Design to study Graphic Design, and I went to Ohio University for Journalism (and more Spanish). I interviewed him for magazine stories I was writing. He wrote me 18 haikus when I turned 18.</p>
      <p>After college, we both moved back to Chicago and spent the next year adventuring and learning the ins and outs of the city: late-night bike rides, all-day music festivals, dive bars, and a lot of brunches.</p>
      <p>We were at a holiday party when Dan took me outside, kissed me, and said, ?People are asking if we?re dating. What should I tell them??</p>
