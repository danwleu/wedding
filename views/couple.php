      <h3>Daniel</h3>
      <p>I am not a spontaneous man.
      <p>I didn?t get my driver?s license until more than a year after I was legally able to; I don?t buy anything off Amazon that has less than 20 reviews I can read. And more often than not, I?ll probably only be halfway through my main course when everyone else at the table is finishing dessert. In fact, the only thing I?ve ever purposefully done off the cuff is ask Katie Scarlett Brandt to be my wife.</p>
      <p>I had been racking my brain for weeks trying to figure out how I was going to propose to Katie. I?d bought the ring a month earlier and had been carrying it around in my pocket everywhere we went. A few ideas were floating around (most of them involving elaborate fake alien landings or reenacting Guns n? Roses' ?November Rain? video), but nothing really seemed to stick.</p>
      <p>Then one evening, Katie had just gotten back from another 13-hour drive out to Pine Ridge Indian Reservation in South Dakota, and I was probably sad at myself for not utilizing that weekend alone to convert our apartment into an 10,000-gallon saltwater aquarium, housing a faithful recreation of the shipwreck from "The Little Mermaid" but with our dog Charlie dressed as Flounder and a surprise engagement ring poking out of the sand instead of a fork.*</p>
      <p>So we both wanted sushi. We grabbed a bottle of wine and walked to our favorite place on the corner of our street. She told me about her trip. I lied and told her I?d had a productive weekend. We talked until the restaurant finally kicked us out, and decided to go for a walk around the neighborhood. After a few blocks, I put my hand in my pocket and felt the ring box.</p>
      <p>Nobody else was around. And I realized that the night we had just had was exactly the type of night I wanted us to have for the rest of our lives. I took a deep breath and got down on one knee to plead my case.</p>
      <p>However, immediately following my heartfelt ?Will You Marry Me??, an inebriated gentleman on a mountain bike came barreling down the sidewalk straight towards us, stopping abruptly less than 1 foot away to ask us whether or not we all were ?having a good night? and ?what?s up."</p>
      <p>Katie was in shock (I like to think from the proposal) and I, not wanting her to answer the Mountain Biker?s questions before mine, jumped up and informed him that we were having a great night, and not much was up but we would let him know if it turned out that anything was up later. Katie said nothing, and the Mountain Biker went on his way.</p>
      <p>Gathering the courage to propose to the same woman for a second time in the course of 60 seconds, I returned to my position on the sidewalk and brought the ring back out. ?Will You Marry Me??</p>
      <p>She said yes. I confirmed. We celebrated.</p>
      <p>I knew immediately that this was the best decision I?d made in my life, and have started to learn that I can take time when I need it. Eccentricities can turn into strengths as long as you have the right counterpart. I know I?ve found mine, and that as long as we?re together we?ll be alright.</p>
      <p>In the distance, the Mountain Biker ran into a chain link fence on the opposite side of the street, and yelled to let us know he, too, was going to be alright.</p>
